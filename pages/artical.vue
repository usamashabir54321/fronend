								<h3 id="introduction">Introduction </h3>	
								<p>
								Zinc (Zn) malnutrition is widespread in resource-poor populations of the world. It weakens the immune function, increases the vulnerability to infection, and affects pregnancy in women as well as the physical growth of children (Roohani et al., 2013; Millward, 2017). Young children, pregnant and lactating women are the most affected due to Zn malnutrition (Hess, 2017). Zn malnutrition can be overcome by taking supplements and dietary intervention. Zn supplementation is convenient especially for effected populations, however, it is not cost-effective intervention (Meenakshi et al., 2007). Dietary intervention is a sustainable long-term intervention intended for the intake of diverse diets including greater consumption of animal-source foods, commercial fortification and biofortification (Bouis, and Saltzman, 2017). Zn Biofortification can enhance Zn status of cereals consumed by rural poor people and can be carried out through plant breeding, transgenic approaches, chemical fertilizers, and with plant growth-promoting rhizobacteria (PGPR) inoculation (White and Broadley, 2005; Rana et al., 2012; Velu et al., 2014; Gregory et al., 2017; Garg et al., 2018). Application of PGPR is a novel biotechnological approach through which cereals can be fortified by enhancing the nutrient bioavailability and uptake (Rana et al., 2012; Hussain et al., 2018; Mumtaz et al., 2018). 
								Bioavailability of Zn for plant uptake and accumulation in dietary foods is dependent on the Zn concentration. An increase in soil pH decreases the Zn solubility and its availability to plants. Zn fertilizers are applied to fulfill the Zn deficiency in plant, however, their greater fraction can become unavailable to plants due to various edaphic factors and these can be transformed into available forms using efficient ZSB strains (Cakmak, 2008; Alloway, 2009; Khanghahi et al., 2018). The PGPR having the ability to solubilize insoluble Zn are called Zn solubilizing bacteria (ZSB) (Saravanan et al., 2007; Mumtaz et al., 2017). ZSB secrete organic acids which chelate the bounded Zn and make it available to crop plants (Fasim et al., 2002; Saravanan et al., 2007; Vidyashree et al., 2018; Mumtaz et al., 2019). Numerous ZSB species of genera viz. Acinetobacter, Bacillus, Cyanobacteria, Gluconacetobacter, Pseudomonas, and Serratia have been reported for their ability to solubilize non-labile-Zn in soil (Saravanan et al., 2007; Mumtaz et al., 2017; Vidyashree et al., 2018; Khanghahi et al., 2018). Among these bacterial genera, Bacillus spp. was the most dominant to solubilize Zn and to promote crop growth, yield and improving nutrients accumulation in grains.  
								Zinc solubilizing Bacillus strains are gram-positive plant-associated bacteria having the ability to solubilize non-labile minerals and secrete growth-promoting metabolites that enhance plant growth, nutrient availability and suppress soil-borne plant pathogens (Chen et al., 2007; Meena et al., 2016; Mumtaz et al., 2017, 2019). Such strains can survive in extremely adverse environments because of their prospective endospore formation and variable fatty acid configurations (Diomande et al., 2015). These efficient strains can enhance the Zn availability to fulfill the requirement of the plant and thus, helpful in cereal grains biofortification (Sharma et al., 2012). Previously, various Zn solubilizing Bacillus strains viz. Bacillus sp. (Shakeel et al., 2015; Mumtaz et al., 2017, 2018), B. aryabhattai (Ramesh et al., 2014; Mumtaz et al., 2017), B. thuringiensis (Khande et al., 2017), B. cereus (Khande et al., 2017), B. firmus, B. amyloliquefaciens (Sharma et al., 2012) and B. subtilis (Mumtaz et al., 2017, 2018) were reported as potential candidates for Zn biofortification in cereals. These ZSB strains biofortified the cereals grains using different mechanisms including biological nitrogen fixation from the atmosphere, solubilization of non-labile mineral compounds, production of phytohormones, 1-aminocyclopropane-1-carboxylate deaminase activity, production of siderophores, and antifungal activities which promote yield and grain quality of cereals (Meena et al., 2016; Mumtaz et al., 2017; Dinesha et al., 2018). These strains can be used as biofertilizers that synergistically promote nutrient absorption and accumulation in grains (Vaid et al., 2014; Mumtaz et al., 2017). 
								Biofortification of cereals through inoculation with ZSB strains is an emerging biotechnological approach that can promote grain quality and human health. It is well understood that the ZSB strain can increase the Zn solubility in soil and its accumulation in grains but its role under Zn deficient soils is poorly understood. Considerable research is needed in this area to recognize novel strains as well as their role for biofortification of cereals under Zn deficient soil conditions. The current experiment describes the potential of selected Zn solubilizing Bacillus strains possessing multiple plant growth-promoting characteristics for improving growth, grain yield, and biofortification of maize under native soil Zn conditions.	
								</p>
								<h6>Materials and Methods</h6>
								<h3 id="bacterial">Collection of bacterial strains and preparation of inoculum</h3>
								<p>Four Zn solubilizing Bacillus strains viz. Bacillus sp. ZM20, Bacillus aryabhattai ZM31, Bacillus subtilis ZM63 and Bacillus aryabhattai S10 (Genbank accession numbers KX086260, KX788860, KX788861, and KX788862, respectively) were obtained from the gene bank of Soil Microbiology and Biotechnology Laboratory, Department of Soil Science, the Islamia University of Bahawalpur. The strains were grown in Dworkin and Foster (DF) minimal media modified with ZnO (0.1% of Zn w/v) as described by Mumtaz et al. (2017) and incubated at 30±1°C under shaking (100 rpm) conditions (Model SI9R-2, Shellab-USA) for 48h. After incubation, the bacterial cells were harvested by centrifugation at 9000 rpm and 22 °C for 20 minutes (Model: UNIVERSAL 320R, Hettich-Germany). The supernatant was discarded, and the pellets were re-suspended in sterilized distilled water. This washing procedure was repeated, and the pellets were dissolved in sterilized distilled water to get uniform cell density (OD = 0.45; cell count 108 CFU mL-1). The final cultures with a uniform population were taken in a sterile flask and used for inoculation. </p>
								<h3 id="experimental">Experimental management </h3>
								<p>The experiment was conducted in farmer field located at Latitude: 29.46N, Longitude: 71.70E, and 115 m elevations above the Arabian Sea level. Before crop sowing, a composite soil sample from 0-20 cm depth was taken and analyzed for physicochemical characteristics by following standard procedures reported by Ryan et al. (2007). Available Zn concentration in the soil before crop sowing was estimated by following diethylene triamine penta-acetic acid (DTPA) extraction method (Lindsay and Norvell, 1978). The Zn concentration in extractant was determined in an Atomic Absorption Spectrophotometer (Agilent Technologies, Australia) using Zn lamp. For determination of total Zn concentration in soil, 0.5 g of soil was digested with hydrofluoric acid (HF) and perchloric acid (HCIO4) and analyzed through Atomic Absorption Spectrophotometer (Yawar et al., 1965). The field soil was sandy loam and low in organic matter, nitrogen (N) and phosphorus (P) but contains enough potassium (K). The bacterial strains along with their co-inoculation combinations were inoculated on maize seeds of cultivar Pioneer-30Y87 (Pioneer Seed Ltd, Pakistan) by preparing slurry with peat, inoculum (bacterial culture) and sugar solution (10%) in the ratio of 5:4:1. For co-inoculation formulation, broth of respective cultures was used in the ratio of 1:1 for slurry preparation. Uninoculated control was maintained through coating seed with peat, control broth (without culture) and sugar solution. Maize seeds from each treatment were sown on four ridges of 75 cm apart and thinning was performed after 15 days of emergence through pulling out the extra/ weak plants to maintain ten plants at a distance of 20-25 cm in each ridge. 
									The recommended dose of N, P, and K (120: 90: 60 kg ha-1) was applied in the form of urea, diammonium phosphate (DAP) and sulfate of potash (SOP), respectively. A full dose of P, K and half of N was applied at sowing time. The remaining half dose of N was given at the anthesis stage. The experiment was conducted by applying treatment in Randomized Complete Block Design (RCBD) with three replications. Irrigation need of each plot was fulfilled through flooding the field with good quality underground irrigation water (Ayers and Westcot, 1985). Thinning was done after germination to maintain plant density. All standard agronomic practices were carried out as and when required. Growth and yield contributing attributes were recorded on harvest at maturity. Plant and grain samples were analyzed to measure biofortified nutrient concentrations. At physiological maturity, data about growth and yield parameters were recorded. For determination of SPAD value, ten mature leaves (3rd from flag leaf) from different plants were selected randomly and reading was noted by using SPAD meter model CL-01 (Hansatech Instruments Ltd, England). For the determination of N, P, K, Fe and Zn, in maize straw and grains, samples from each treatment and replication were oven-dried at 67 °C and wet digested following the method as described by Wolf (1982).
									For the determination of P concentration in digested plant samples, the standard procedure of Ryan et al. (2001) was followed. Flame photometer (BWP Technologies, UK) was used to determine K in digested plant samples. The N, Fe, and Zn were determined by using commercial service of Central Hi-Tech Laboratory, University of Agriculture Faisalabad, Punjab, Pakistan. The N concentration in plant extract was determined following the standard procedure of the Kjeldahl method as described by Ryan et al. (2001). For Fe and Zn analysis, samples were analyzed by using Atomic Absorption Spectrophotometer (Agilent Technologies, Australia) as described by Helrich (1990). 
								</p>
								<h3 id="statistical-analysis">Statistical analysis</h3>
								<p>The statistical method was developed to evaluate the effect of sole and co-inoculation with ZSB strains on growth, yield, and quality of maize. The group of variables were randomly split into bacterial inoculation treatment and collected data were analyzed by using one-way analysis of variance technique (ANOVA) and means were compared by Least Significant Difference (LSD) Tests at 5% level of significance (Steel et al., 1997) through computer software Statistix v. 8.1 (Analytical Software, Tallahassee, FL, USA).</p>
								<h6>Results</h6>
								<h3 id="characterization">Field Soil Characterization</h3>
								<p>The experimental field soil used in this study was characterized by physicochemical properties. The result revealed that the experimental soil was sandy loam (70% sand, 16% silt and 14% clay) alkaline in nature (pH 8.1) having 0.28 dS m-1 electrical conductivity of soil extract and 0.88% organic matter contents. Before sowing, soil showed total N contents up to 0.05%, available P up to 4.6 mg kg-1, and extractable K up to 169 mg kg-1. There was 46.5 mg kg-1 total Zn concentration in the soil while 3.2 mg kg-1 of Zn was present in the available form (data is not given). </p>
								<h3 id="growth-attributes">Growth attributes</h3>
								<p>Maize growth parameters, including SPAD (Soil-Plant Analyses Development) unit value, plant height and shoot dry weight in retort to sole or co-inoculation with ZSB strains are given in Table 1. The inoculation/co-inoculation gave a significant increase in growth attributes of maize in terms of SPAD unit value, plant height and shoot dry weight. Uninoculated control showed significantly lowest SPAD value of 50.7. The increase in the SPAD unit value of maize was observed due to both inoculation and co-inoculation, however, co-inoculation treatments were more effective to show an increase in SPAD value except the combination of Bacillus sp. ZM20 and B. aryabhattai S10. The maximum SPAD value was observed due to the co-inoculated combination of B. aryabhattai ZM31 and B. aryabhattai S10 that showed 65.3 of SPAD value. The co-inoculation combination of B. aryabhattai S10 × B. subtilis ZM63 also showed better SPAD value (61.4). Uninoculated control showed the lowest plant height up to 220.5 cm and shoot dry weight up to 187.6 g. The sole inoculation of most of the strains gave a significant increase in maize height and shoot dry weight, however, co-inoculation was more effective as compared to sole inoculation. The maximum plant height of 255.9 cm was recorded in the treatment involving the combined use of B. aryabhattai ZM31 and B. subtilis ZM63 which was statistically similar to co-inoculation of B. aryabhattai S10 × B. subtilis ZM63 (249.8 cm). Combined application of B. aryabhattai ZM31 and B. subtilis ZM63 also gave maximum shoot dry weight 216 g which was statistically similar to co-inoculation with Bacillus sp. ZM20 × B. subtilis ZM63. </p>
								<h3 id="yield-attributes">Yield attributes</h3>
								<p>Yield attributes in terms of cob length, cob dry weight and 100-grains weight were enhanced when treated with sole and/or co-inoculation (Table 2). Most of the treatments were non-significant to each other but statistically different from uninoculated control. Co-inoculation with B. aryabhattai ZM31 and B. subtilis ZM63 resulted in maximum cob length of 17 cm while minimum cob length was 15.3 cm shown by uninoculated control. Uninoculated control also showed a minimum cob dry weight of 131.4 g and a 100-grains weight of 18.2 g. Co-inoculation with B. aryabhattai ZM31 and B. subtilis ZM63 showed maximum cob dry weight of 158.3 g which was non-significant with sole inoculation of B. aryabhattai S10, however, these treatments were statistically significant with uninoculated control. The maximum 100-grains weight of 22.8 g was observed due to co-inoculation with B. aryabhattai ZM31 and B. subtilis ZM63 followed by co-inoculation with Bacillus sp. ZM20 and B. subtilis ZM63 (22.4 g). These combinations were non-significant to each other but significantly different from uninoculated control. 
								The data regarding the effect of inoculation and co-inoculation on stover and grain yield and harvest index are given in Table 3. Results revealed that uninoculated control showed a minimum stover yield of 17410 kg ha-1, grain yield of 8598 kg ha-1, and harvest index of 26.6%. The maximum grain yield of 9826 kg ha-1 was obtained due to co-inoculation with B. aryabhattai ZM31 and B. subtilis ZM63 followed by the combined application of Bacillus sp. ZM20 × B. subtilis ZM63 that showed 9741 kg ha-1 of grain yield. These treatments were also non-significant with combined application of B. aryabhattai S10 and B. subtilis ZM63. Co-inoculated combination of B. aryabhattai ZM31 and B. subtilis ZM63 showed the maximum stover yield of 19599 kg ha-1 and harvest index of 31%. This combination was statistically similar to the co-inoculation of Bacillus sp. ZM20 and B. subtilis ZM63, however, these treatments were significantly different from uninoculated control. </p>
								<h3 id="concentration">Macronutrient’s concentration</h3>
								<p>Inoculation with Zn solubilizing Bacillus strains improved N, P, and K concentration in straw and grains of maize (Tables 4). Uninoculated control was lowest to show N, P, and K concentration in maize straw up to 0.83%, 0.73%, and 1.25%, respectively and in maize grains up to 1.15%, 0.77%, and 0.62%, respectively. The maximum N concentration in straw and grains was 1.57% and 2.34%, respectively, shown by a co-inoculation combination of Bacillus sp. ZM20 × B. aryabhattai ZM31. This treatment was non-significant with a co-inoculation combination of B. aryabhattai ZM31 × B. subtilis ZM63, however, these treatments were significantly different from uninoculated control. Co-inoculation with B. aryabhattai S10 and B. subtilis ZM63 reported the highest P concentration in maize straw and grain up to 1.05% and 0.94%, respectively and was non-significant with other sole and co-inoculation combinations, however, it was significantly different uninoculated control. Co-inoculation with B. aryabhattai S10 and B. subtilis ZM63 reported maximum K concentration in straw (1.83%) and grain (0.85%) followed by co-inoculation with Bacillus sp. ZM20 and B. subtilis ZM63. These treatments were non-significant to each other but significantly different from uninoculated control. </p>
								<h3 id="grains">Zn and Fe concentration in grains</h3>
								<p>Co-inoculation with B. aryabhattai ZM31 and B. subtilis ZM63 reported the highest Fe concentration of 56.5 mg kg-1 in maize grains (Figure 1) followed by co-inoculation with B. aryabhattai S10 and B. subtilis ZM63. Uninoculated control showed the lowest Fe concentration of 42.2 mg kg-1 in maize grains. Co-inoculation with B. aryabhattai ZM31 + B. subtilis ZM63 and B. aryabhattai S10 + B. subtilis ZM63 reported maximum Zn concentration in grains of 52.0 mg kg-1 and 51.36 mg kg-1, respectively. Zn concentration in maize grains was lowest in case of inoculated control that showed 44.892 mg kg-1</p>
								<h6>Discussion </h6>
								<h3 id="growth">Increase in maize growth and yield due to co-inoculation with Bacillus strains </h3>
								<p>Zinc is the key component of plants and required for their growth and development. Its deficiency is most common in crops that cause a reduction in crop yield. Application of Zn fertilizers is underutilized in many countries including Pakistan and also not be cost-effective. Moreover, Zn-fertilizers become converted into insoluble form soon after their applications due to alkaline nature of the soil. Growing crops on such Zn-deficient soil could hinder crop growth and produced staple grains that have resulted in Zn-deficient. Inoculation with Zn solubilizing bacteria is an effective strategy to solubilize the insoluble Zn compound that increases the nutrient availability in soil and crop productivity (Mumtaz et al., 2018). These bacteria use various direct and indirect mechanisms that can contribute to enrich the cereals grains with Zn. The present investigation was aimed to biofortify the maize grains along with increasing crop productivity through the application of Zn solubilizing Bacillus strains (Bacillus sp. ZM20, Bacillus aryabhattai ZM31, Bacillus subtilis ZM63 and Bacillus aryabhattai S10) Previously, we have reported the multiple growth-promoting traits of these Zn solubilizing Bacillus strains and their potential to increase growth, yield, and nutrient uptake in maize (Mumtaz et al., 2017, 2018).  
								In the present study, sole and co-inoculation with Zn solubilizing Bacillus strains promoted maize growth and yield, however, co-inoculation treatments showed better increase in maize growth and yield that might be due to better competency of the strains in plant growth-promoting attributes e.g. solubilization of Zn and P minerals, production of phytohormones, siderophore, urease, catalase activity, and ammonia and exopolysaccharides production ability (Mumtaz et al., 2017; Dinesha et al., 2018). Microbial solubilization of P and Zn through secretion of organic acids may cause a drop in pH that played a key role in increasing their solubility and uptake (Ramesh et al., 2014). Co-inoculation with these strains may result in mutualistic interaction that altered root morphology to acquire more nutrients in the plant to increase yield. These Zn solubilizing Bacillus strains were well-reported for indole acetic acid (IAA) that plays a very important role in plant-microbe interactions that stimulate and facilitate plant growth. Microbial secreted IAA interacts with plant developmental processes which may alter the endogenous pool of plant IAA and induces cell elongation and cell division (Spaepen et al., 2007). Moreover, several studies related to Zn solubilizing bacterial strains were reported to promote plant growth parameters (Dubey et al., 2014; Chauhan et al., 2016; Pandey et al., 2018). 
								In the present, the increase in maize growth and yield could also be due to the increase in nutrient uptake and their accumulation in various plant parts due to co-inoculation with Zn solubilizing Bacillus strains. The N uptake was more due to inoculation with Bacillus sp. ZM20 and B. aryabhattai ZM31 that might be due to the presence of nitrogenase enzyme in these strains and having the ability to fix atmospheric N that may facilitate its uptake (Spaepen et al., 2007). In current investigations, co-inoculation with B. aryabhattai S10 and B. subtilis ZM63 reported the highest uptake of P, K, and Fe in maize grains. Highest Zn biofortification in maize stover and grains was observed from a combined use of B. aryabhattai ZM31 and B. subtilis ZM63. Our findings are supported by the results of Rana et al. (2012), Ramesh et al. (2014) and Abaid-Ullah et al. (2015). Macronutrient uptake had a positive impact on micronutrient uptake which correlated to their accumulation in grains (Cakmak et al., 2010). Translocation and mobilization of Fe and Zn in grains depend on their concentration in vegetative tissue, N status, and different species and cultivars. Microbial production of the organic acid may cause a reduction in pH and shifted the dynamic equilibrium of minerals from non-labile to labile form and may promoted nutrient accumulation in plants (Wani et al., 2007). </p>
								<h3 id="biofortification">Maize biofortification due to co-inoculation with Bacillus strains </h3>
								<p>The present study revealed that the co-inoculation with Zn solubilizing Bacillus strains enriches the maize grains with Zn and Fe that might be due to the increase in the availability of Zn and Fe for plant uptake. The co-inoculation combination of aryabhattai ZM31 + B. subtilis ZM63 and aryabhattai ZM31 and B. subtilis ZM63 showed the promising result to biofortify the maize grain with Zn and Fe. These co-inoculation combinations might be more compatible and competative to solubilize the insoluble native soil Zn contents and improved it uptake and accumulation in maize grains as compared to other sole and co-inoculation combination. Bacillus strains also have the ability to produce siderophores which is important for solubilization, mobilization, and phytoextraction of metals (Whiting et al., 2001). The increase in Zn and Fe concentration in maize grains due to inoculation may also cause a reduction in antinutrients agent e.g. phytic acid, gluten, tannins, oxalates, lectins, leptins, and saponins which is helpful to improve the bioavailability of nutrients for human consumption. Phytic acid in grains is not bio-available and binds to Fe and Zn in grains and makes them unavailable to humans (Thompson, 1989). Previous findings of Ramesh et al. (2014) reported the reduction in phytic acid accumulation in grains which could be a possible reason for the biofortification of maize in the current study. The particular mechanism for biofortification upon inoculation is unknown however, it is thought to be due to their growth-promoting characteristics that modulate root morphology, improve nutrient acquisition, and accumulation in grains. 
								Biofortification of maize through Bacillus strains has immense importance to mitigate micronutrients malnutrition and illness in developing countries (Bouis and Welch, 2010). As people consume cereal-based diets to meet daily nutritional requirements which contain too low Zn concentration. Cereal grains must contain at least 45 mg kg-1 of Zn concentration for a significant impact on adult health by assuming daily intake of 400 g of chapatti made from cereal flour (Cakmak, 2008). Zn concentration in grains due to inoculation with Bacillus strains in the current study is relatively high for maize, even in the control, is above the minimum level of required Zn concentration to meet daily intake. Its accumulation in grains due to inoculation in the current study might have large implications in terms of remediation of malnutrition in rural population. Co-inoculation of Zn solubilizing Bacillus strains have a significant impact on crop productivity and biofortification of maize and potentially to be promoted as bio-inoculants to overcome the nutrient deficiency in cereals. </p>
								<h3 id="conclusion">Conclusion</h3>
								<p>The combined use of Bacillus strains viz. B. aryabhattai ZM31 & S10 and B. subtilis ZM63 were found highly effective for the biofortification of maize along with improvement in growth and yield parameters. These inoculants would be effective in the context of increasing food quality and reducing the use of chemical fertilizers in agriculture. The current study clearly demonstrates that tested Bacillus strains have the potential to biofortify maize grains under field conditions and are recommended to use as potential bio-inoculant for Zn biofortification under nutrient-deficient soils.</p>
