								<br /><br />
								<h3 id="abstract">Abstract</h3>	
								<p>
								Zinc (Zn) deficiency in major food crops has been considered as an important factor affecting crop production and subsequently the human health. The application of Zn solubilizing bacteria could be a sustainable agronomic approach to increase the soil available Zn. The present field study was conducted at two different sites (Farmers’ fields at Bahawalpur, Punjab, Pakistan) to evaluate the biofortification potential of four Zn solubilizing Bacillus strains viz. Bacillus sp. ZM20, Bacillus aryabhattai ZM31, Bacillus subtilis ZM63, and Bacillus aryabhattai S10, alone as well as in different combination. Separate as well as combinations of Zn solubilizing Bacillus strains, significantly, improved the plant growth, yield, and grain nutrient concentrations at both experimental sites. However, more increase in maize growth, yield and biofortified Zn and iron (Fe) concentration in grains were obtained due to co-inoculated combinations. Co-inoculation with B. aryabhattai S10 and B. subtilis ZM63 had the maximum Fe concentration (56.5 mg kg-1) in grains with an increase of 34% at Site-I compared to respective un-inoculated control. While the maximum increase in Zn concentration in maize grains (23% over respective un-inoculated control at Site-II) was recorded due to combined use of B. aryabhattai ZM31 and B. subtilis ZM63. These results suggested that co-inoculation with Zn solubilizing Bacillus strains expressed synergistic effects to increase nutrients acquisition and to promote growth and yield of maize. It is recommended to use the consortia of B. aryabhattai ZM31 and B. subtilis ZM63 as potential bio-inoculants for Zn biofortification of maize under nutrient-deficient soil conditions.
								<br />
								 <b>Keywords:</b> Bacillus strains; biofortification; Zea mays; Zn solubilization; Co-inoculation 
								</p>
								
